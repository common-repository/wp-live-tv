<h3 class="tab-content-title">Gutenberg Block</h3>

<div class="tab-content-section">
    <p>The plugin provide a gutenberg block for displaying the tv player in any page/ post.</p>

    <p>While you are on the post/page edit screen click on gutenberg plus icon to add a new gutenberg block. <br>
        Add <b>TV Player</b> block from <b>Media</b> category.
    </p>

    <p>After selecting the <b>TV Player</b> block, you need to enter the channel id of the tv channel. <br>
        You can change the tv channel by clicking the <b>Change Channel</b> option at the top toolbar.
    </p>
    <img src="<?php echo WPTV_ASSETS . '/images/get-started/tv-player-block.png' ?>" alt="TV Player Block">

    <p>To know how to get the channel id of any TV channel, check the FAQ section.</p>
</div>
