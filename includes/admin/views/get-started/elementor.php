<h3 class="tab-content-title">Elementor Widget</h3>

<div class="tab-content-section">
    <p>The plugin provides a elementor widget for displaying the TV player any page/ post.</p>

    <p>While you are on the post/page elementor edit screen, Add the <b>TV Player</b> widget from<b>basic</b> category.</p>

