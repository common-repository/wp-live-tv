<p>Setting up WP LIVE TV is very easy. This plugin setup itself on the installation. Here are few things that you should consider:</p>

<div class="tab-content-section">
    <p>After activating this plugin it creates a page named <b>TV Channels</b>. The page contains
        <code>[wptv_listing]</code> shortcode.
        The <b>TV Channels</b> page is the default channels archive page, where all the channels will be listed.
    </p>

